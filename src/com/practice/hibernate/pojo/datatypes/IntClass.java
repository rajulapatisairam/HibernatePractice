package com.practice.hibernate.pojo.datatypes;

public class IntClass {

	private int primaryKey;
	private int unSign;
	private int notEmpty;
	private int length1;
	private int emptyAllow;
	private int defaultValue;
	public int getPrimaryKey() {
		return primaryKey;
	}
	public void setPrimaryKey(int primaryKey) {
		this.primaryKey = primaryKey;
	}
	public int getUnSign() {
		return unSign;
	}
	public void setUnSign(int unSign) {
		this.unSign = unSign;
	}
	
	
	public int getNotEmpty() {
		return notEmpty;
	}
	public void setNotEmpty(int notEmpty) {
		this.notEmpty = notEmpty;
	}
	public int getLength1() {
		return length1;
	}
	public void setLength1(int length1) {
		this.length1 = length1;
	}
	public int getEmptyAllow() {
		return emptyAllow;
	}
	public void setEmptyAllow(int emptyAllow) {
		this.emptyAllow = emptyAllow;
	}
	public int getDefaultValue() {
		return defaultValue;
	}
	public void setDefaultValue(int defaultValue) {
		this.defaultValue = defaultValue;
	}
	
	
}
